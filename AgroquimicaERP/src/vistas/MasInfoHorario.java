    public MasInfoHorario(int idHorario) throws SQLException, IOException {
        initComponents();
        this.setLocationRelativeTo(null);
        
        jScrollPane1.getViewport().setBackground(new Color(243,243,243));
        jPanel2.requestFocus();
        
        con = ConexionSQL.getInstance();
        dao = new DAO(con.getConnection());
        this.idHorario = idHorario;
        
        datos = dao.horarios.getH(idHorario);
        this.setData();
    }
    
    public void setData() {
        if(datos != null) {            
            txtIdH.setText(String.format("%03d", idHorario));
            
            txtHI.setText(datos[0]);
            txtHF.setText(datos[1]);
            
            String dias[] = datos[2].split(",");
            for(int i=0; i<dias.length; i++) {
                switch(dias[i]) {
                    case "Lun":
                        chbLun.setSelected(true);
                        break;
                    case "Mar":
                        chbMar.setSelected(true);
                        break;
                    case "Mie":
                        chbMie.setSelected(true);
                        break;
                    case "Jue":
                        chbJue.setSelected(true);
                        break;
                    case "Vie":
                        chbVie.setSelected(true);
                        break;
                    case "Sab":
                        chbVie.setSelected(true);
                        break;
                    case "Dom":
                        chbDom.setSelected(true);
                        break;
                }
            }
            
            txtIdEmpleado.setText(datos[3]);
            dao.horarios.getAll(tblHorarios, Integer.valueOf(datos[4]), idHorario);
        } else
            JOptionPane.showMessageDialog(null, "Error al consultar los datos del horario", "Error", 0);
    }

