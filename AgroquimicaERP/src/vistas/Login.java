    private void disablePaste() {
        JTextComponent.KeyBinding[] newBindings = {
        new JTextComponent.KeyBinding(
          KeyStroke.getKeyStroke(KeyEvent.VK_C, InputEvent.CTRL_MASK),
          DefaultEditorKit.copyAction),
        new JTextComponent.KeyBinding(
          KeyStroke.getKeyStroke(KeyEvent.VK_V, InputEvent.CTRL_MASK),
          DefaultEditorKit.beepAction),
        new JTextComponent.KeyBinding(
            KeyStroke.getKeyStroke(KeyEvent.VK_X, InputEvent.CTRL_MASK),
            DefaultEditorKit.cutAction)
        };
            
        Keymap k1 = txtUsuario.getKeymap();
        JTextComponent.loadKeymap(k1, newBindings, txtUsuario.getActions());
        
        Keymap k2 = txtPassword.getKeymap();
        JTextComponent.loadKeymap(k2, newBindings, txtPassword.getActions());
    }

