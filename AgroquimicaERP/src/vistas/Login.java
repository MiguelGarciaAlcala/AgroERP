    public void authenticate() {
        String username = txtUsuario.getText();
        String password = String.valueOf(txtPassword.getPassword());
        
        if(username.equals("") || password.equals(""))
            JOptionPane.showMessageDialog(null, "Debe informar los campos requeridos", "Hay campos vacíos", 0);
        else {
            String nombre = dao.usuarios.exists(username, password);
            if(nombre != null) {
                Menu home;
                home = new Menu(nombre);
                home.setVisible(true);
                this.dispose();
            } else
                JOptionPane.showMessageDialog(null, 
                        "No se pudo iniciar sesión con el usuario " + username + ":\n verifique los datos.", "Login fallido", 2);
        }
    }
