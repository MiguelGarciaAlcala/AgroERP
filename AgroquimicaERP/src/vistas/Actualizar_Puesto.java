    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre = txtNombre.getText();
        if (!nombre.equals("")) {
            int ans = JOptionPane.showConfirmDialog(null,
                    "Se actualizará la Ciudad\n¿Deseas continuar?", "Advertencia", 2);

            if (ans == JOptionPane.YES_OPTION) {
                if (dao.deptos.update(new Departamento(puesto.getIdPuesto(), nombre))) {
                    txtFormat.borrar_tabla(departamentos);
                    dao.ciudades.getAll(departamentos);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Se deben informar todos los campos", "Faltan datos", 2);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

