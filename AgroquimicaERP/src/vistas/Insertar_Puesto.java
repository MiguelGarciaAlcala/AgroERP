    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int idPuesto = Integer.parseInt(txtIdPuesto.getText());
        String nombre = txtNombre.getText();
        double salarioMin = Double.parseDouble(jSpinner1.getValue().toString());
        double salarioMax = Double.parseDouble(jSpinner2.getValue().toString());

        if (!nombre.equals("")) {
            if (dao.puestos.insert(new Puesto(idPuesto, nombre, salarioMin, salarioMax))) {
                txtFormat.borrar_tabla(puestos);
                dao.puestos.getAll(puestos);
                txtNombre.setText("");
                jSpinner1.setValue(0);
                jSpinner2.setValue(0);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Se deben informar todos los campos", "Faltan datos", 2);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

